static void connectWifi() {
  setLed(CRGB::Red);
  WiFi.mode(WIFI_STA);
  WiFi.begin(WIFI_SSID, WIFI_PASSWORD);
  while (WiFi.status() != WL_CONNECTED) { delay(250); }
  setLed(CRGB::Blue);
}

void setup() {
  Serial.begin(115200);
  FastLED.addLeds<SK6812, LED_PIN, GRB>(leds, NUM_LEDS);
  FastLED.setBrightness(40);

  // RS-232 tap. UART_CONFIG/UART_BAUD are placeholders until sniffed.
  OMR.begin(UART_BAUD, UART_CONFIG, UART_RX_PIN, UART_TX_PIN);

  connectWifi();

  ws.begin(BACKEND_HOST, BACKEND_PORT, WS_PATH);
  ws.onEvent(onWsEvent);
  ws.setReconnectInterval(3000);
}

// Accumulate bytes; flush on an inter-byte idle gap (a crude frame boundary
// that works for record-oriented serial output). Tune FRAME_IDLE_MS after
// observing real timing.
static const uint32_t FRAME_IDLE_MS = 30;
static uint8_t frameBuf[512];
static size_t  frameLen = 0;
static uint32_t lastByteMs = 0;

void loop() {
  ws.loop();

  while (OMR.available()) {
    int b = OMR.read();
    if (b < 0) break;
    if (frameLen < sizeof(frameBuf)) frameBuf[frameLen++] = (uint8_t)b;
    lastByteMs = millis();
    setLed(CRGB::Purple);   // brief flash: bytes flowing
  }

  if (frameLen > 0 && (millis() - lastByteMs) > FRAME_IDLE_MS) {
#if SNIFF_MODE
    emitRaw(frameBuf, frameLen);
#else
    // handleFrame(frameBuf, frameLen);
#endif
    frameLen = 0;
    setLed(wsConnected ? CRGB::Green : CRGB::Amber);
  }
}
